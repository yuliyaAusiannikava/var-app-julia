package by.itstep.oop.inh.animal;

public class Animal {
    private long id;
    private String name;

    public void setId(long id) {
        this.id = id;
    }
    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
